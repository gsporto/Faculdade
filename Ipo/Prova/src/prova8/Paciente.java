package prova8;

public class Paciente {
	private String nome;
	private int idade;
	
	public String getNome() {
		return nome;
	}
	public void setNomePaciente(String nome) {
		this.nome = nome;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdadePaciente(int idade) {
		this.idade = idade;
	}
	
}
